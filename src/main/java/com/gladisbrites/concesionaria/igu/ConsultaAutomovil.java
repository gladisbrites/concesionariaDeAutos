    private void cargarTabla() {
        
        DefaultTableModel tabla=new DefaultTableModel(){
            //filas columnas no sean editables
                 @Override
                 public boolean isCellEditable(int row, int column){
                     return false;
                  }
             };
        
        //establecemos nombres de las columnas
        
        String titulos[]={"Id","Modelo","Marca", "Motor","Color","Patente",
            "Cant.Puertas" };
        tabla.setColumnIdentifiers(titulos);
       
        //carga datos de la base de datos y mostramos en la tabla 
        List<Automovil>listaAutos = controladoraLogica.traeAuto();
        if (listaAutos!=null){        
            for(Automovil auto:listaAutos){
              Object[] objeto ={
                  auto.getId(),auto.getModelo(),auto.getMarca(),auto.getMotor(),auto.getColor(),
                  auto.getPatente(),auto.getCantPuertas()
                   };
              tabla.addRow(objeto);
            }
        }
        
        tablaAutos.setModel(tabla);

    }
    
    
    public void mostrarMensaje(String mensaje, String tipo, String titulo){
                
        JOptionPane optionPane =new JOptionPane(mensaje);
        if(tipo.equals("Info")){
                 optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }
        else if(tipo.equals("Error")){
                    optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);

        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);

    
    }
